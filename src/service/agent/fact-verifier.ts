import { DynamicStructuredTool } from "@langchain/core/tools";
import { z } from "zod";
import { ChatOpenAI } from "@langchain/openai";
import { HumanMessage, SystemMessage } from "@langchain/core/messages";

export interface FactVerificationResult {
  isAccurate: boolean;
  confidence: "high" | "medium" | "low";
  issues: Array<{
    type:
      | "hallucination"
      | "exaggeration"
      | "missing_citation"
      | "inference"
      | "contradiction";
    description: string;
    severity: "critical" | "moderate" | "minor";
  }>;
  verifiedFacts: string[];
  recommendation?: string;
}

/**
 * Fact Verifier: Verifies if the generated answer is accurate and not hallucinating
 */
export async function verifyFacts(
  userQuery: string,
  generatedAnswer: string,
  sourceDocuments: Array<{
    id: string;
    score: number;
    metadata: Record<string, any>;
    text: string;
  }>,
  model: string = "gpt-4o"
): Promise<FactVerificationResult> {
  const systemPrompt = `You are an expert fact-checker that verifies whether a generated answer is accurate and not hallucinating.

Your task is to:
1. Compare the generated answer against the source documents
2. Identify any hallucinations, exaggerations, or unsupported claims
3. Verify that all facts in the answer can be traced back to the source documents
4. Check for over-generalization, inference beyond what's stated, or contradictions

VERIFICATION CRITERIA:
- ACCURATE: All claims can be directly traced to source documents
- HALLUCINATION: The answer contains information not present in source documents
- EXAGGERATION: The answer inflates numbers, counts, or severity beyond what documents state
- INFERENCE: The answer makes logical leaps not supported by the documents
- CONTRADICTION: The answer contradicts information in the source documents
- MISSING_CITATION: The answer makes claims without clear document support

ISSUE SEVERITY:
- CRITICAL: Factual errors that significantly mislead (wrong counts, invented incidents, false claims)
- MODERATE: Exaggerations or inferences that distort the truth but aren't completely false
- MINOR: Minor imprecisions or missing citations that don't significantly impact accuracy

OUTPUT FORMAT:
Return a JSON object with:
- isAccurate: boolean (true if answer is fully accurate, false if any issues found)
- confidence: "high" | "medium" | "low" (confidence in the verification)
- issues: array of issue objects with type, description, and severity
- verifiedFacts: array of strings (key facts that were verified)
- recommendation: string (optional, recommendation for improvement)`;

  const userPrompt = `User Query: ${userQuery}

Generated Answer:
${generatedAnswer}

Source Documents (${sourceDocuments.length} total):
${sourceDocuments
  .map(
    (doc, idx) => `Document ${idx + 1} (ID: ${
      doc.id
    }, score: ${doc.score.toFixed(3)}):
${doc.text.substring(0, 800)}${doc.text.length > 800 ? "..." : ""}
`
  )
  .join("\n---\n")}

Verify the generated answer against the source documents. Check for:
1. Facts that can be directly traced to documents (verified facts)
2. Any hallucinations, exaggerations, or unsupported claims (issues)

Return your verification as JSON.`;

  try {
    const llm = new ChatOpenAI({
      modelName: model,
      openAIApiKey: process.env.OPENAI_API_KEY,
      temperature: 0,
      modelKwargs: {
        response_format: { type: "json_object" },
      },
    });

    const messages = [
      new SystemMessage(systemPrompt),
      new HumanMessage(userPrompt),
    ];

    const response = await llm.invoke(messages);
    const content = response.content?.toString() || "{}";
    const result = JSON.parse(content) as FactVerificationResult;

    // Ensure required fields with defaults
    return {
      isAccurate: result.isAccurate ?? true,
      confidence: result.confidence || "medium",
      issues: result.issues || [],
      verifiedFacts: result.verifiedFacts || [],
      recommendation: result.recommendation,
    };
  } catch (error: any) {
    console.error("Fact verifier error:", error);
    return {
      isAccurate: false,
      confidence: "low",
      issues: [
        {
          type: "hallucination",
          description: `Error during fact verification: ${error.message}`,
          severity: "critical",
        },
      ],
      verifiedFacts: [],
      recommendation: "Manual review recommended due to verification error",
    };
  }
}

/**
 * Creates a LangChain tool for fact verification
 */
export function createFactVerifierTool(model: string) {
  return new DynamicStructuredTool({
    name: "fact_verifier",
    description:
      "ONLY verifies if a generated answer is accurate and not hallucinating by comparing it against source documents. This tool should ONLY be called AFTER a summary has been generated by the summary_generator. Returns verification results with identified issues and verified facts. Does NOT retrieve or summarize documents.",
    schema: z.object({
      userQuery: z.string().describe("The original user query"),
      generatedAnswer: z
        .string()
        .describe("The answer generated by the summary generator"),
      sourceDocuments: z
        .array(
          z.object({
            id: z.string(),
            score: z.number(),
            metadata: z.record(z.string(), z.any()).optional(),
            text: z.string(),
          })
        )
        .describe("The source documents used to generate the answer"),
    }),
    func: async ({ userQuery, generatedAnswer, sourceDocuments }) => {
      const documentsWithMetadata = sourceDocuments.map((doc) => ({
        ...doc,
        metadata: doc.metadata || {},
      }));
      const result = await verifyFacts(
        userQuery,
        generatedAnswer,
        documentsWithMetadata,
        model
      );
      return JSON.stringify(result);
    },
  });
}
